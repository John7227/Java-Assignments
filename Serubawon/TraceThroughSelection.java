/*15 is a value assigned to the variable called X so those are conditions that if 15 falls
under anyone of those then it is true it will then print what is in that condition block of
code since it fall under greater than 10 then Medium will be printed out which this is the
code :*/



public class TraceThroughSelection {
public static void main(String[] args) {

	int X = 15;
	if (X > 20) {
	System.out.print("Big");
	}
	else if (X > 10) {
	System.out.print("Medium");
	}
	else {
	System.out.print("Small");
	}

    }

}